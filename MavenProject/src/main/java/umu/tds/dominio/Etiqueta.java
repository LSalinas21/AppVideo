package umu.tds.dominio;

public class Etiqueta {
	
	private String nombre;

	public Etiqueta(String nombre) {
		
		this.nombre = nombre;
	}
	public Etiqueta() {
		
		this.nombre = "";
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	

}
